
# Igualdad ==
print( 5 == 5)
print( 5 == 3)
print( "hello" == "hello")

# Desigualdad !=
print( 5 != 5)
print( 5 != 3)
print( "hello" != "hello")

# Mayor que >
print( 5 > 5)
print( 5 > 3)

# Menor que <
print( 5 < 5)
print( 5 < 3)

# Mayor igual >=
print( 5 >= 5)
print( 5 >= 3)

# Menor que <=
print( 5 <= 5)
print( 5 <= 3)
